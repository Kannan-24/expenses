                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });

            // Add scroll spy functionality
            const sections = document.querySelectorAll('section[id]');
            const navLinks = document.querySelectorAll('a[href^="#"]');

            window.addEventListener('scroll', () => {
                let currentSection = '';
                sections.forEach(section => {
                    const sectionTop = section.getBoundingClientRect().top;
                    if (sectionTop <= 100) {
                        currentSection = section.getAttribute('id');
                    }
                });

                navLinks.forEach(link => {
                    link.classList.remove('bg-blue-100', 'border-blue-300');
                    if (link.getAttribute('href') === '#' + currentSection) {
                        link.classList.add('bg-blue-100', 'border-blue-300');
                    }
                });
            });

             // Reading progress indicator
            const progressBar = document.createElement('div');
            progressBar.className = 'fixed top-0 left-0 h-1 z-50 transition-all duration-300 bg-gradient-to-r from-blue-500 to-purple-500';
            document.body.appendChild(progressBar);

            window.addEventListener('scroll', () => {
                const scrolled = (window.scrollY / (document.documentElement.scrollHeight - window.innerHeight)) * 100;
                progressBar.style.width = `${Math.min(scrolled, 100)}%`;
            });
        });
